import 'package:flutter/material.dart';
import 'package:shared_preferences/shared_preferences.dart';
import 'package:fashion_mobile/services/theme_service.dart'; // Pastikan Import ini ada
import 'package:fashion_mobile/pages/login.dart';
import 'home.dart';

class SettingsScreen extends StatefulWidget {
  const SettingsScreen({super.key});

  @override
  State<SettingsScreen> createState() => _SettingsScreenState();
}

class _SettingsScreenState extends State<SettingsScreen> {
  // ===== Brand accent =====
  static const _purple = Color(0xFF6B257F);

  // ===== Search =====
  final _searchC = TextEditingController();

  // ===== Dummy states =====
  bool _notif = true;
  // bool _darkMode = false; // HAPUS INI, KITA GANTI LOGIKANYA
  bool _biometric = false;
  bool _autoBackup = true;

  // ===== Expand/Collapse sections =====
  bool _expPref = true;
  bool _expUmum = true;
  bool _expBantuan = true;

  @override
  void dispose() {
    _searchC.dispose();
    super.dispose();
  }

  // Fungsi Logout
  Future<void> _logout() async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.clear(); // Hapus semua data sesi
    if (mounted) {
      Navigator.pushAndRemoveUntil(
        context,
        MaterialPageRoute(builder: (_) => const Login()),
            (route) => false,
      );
    }
  }

  bool _match(String text, String q) {
    if (q.isEmpty) return true;
    return text.toLowerCase().contains(q);
  }

  @override
  Widget build(BuildContext context) {
    final q = _searchC.text.trim().toLowerCase();

    // ===== 1. LOGIKA BARU: AMBIL DARI TEMA GLOBAL =====
    // Cek apakah aplikasi sedang mode gelap?
    final bool isDark = Theme.of(context).brightness == Brightness.dark;

    // Token Warna (Menyesuaikan isDark otomatis)
    final bg = isDark ? const Color(0xFF0B1220) : const Color(0xFFF7FAFF);
    final ink = isDark ? const Color(0xFFE5E7EB) : const Color(0xFF0F172A);
    final muted = isDark ? const Color(0xFF94A3B8) : const Color(0xFF64748B);
    final border = isDark ? const Color(0x1FFFFFFF) : const Color(0x1A0F172A);
    final card = isDark ? const Color(0xCC0F172A) : const Color(0xCCFFFFFF);
    final tile = isDark ? const Color(0xFF111827) : const Color(0xFFF1F5F9);
    final tileBorder = isDark ? const Color(0x22FFFFFF) : const Color(0x110F172A);
    final iconSurface = isDark ? const Color(0xFF0F172A) : Colors.white;

    // === Auto open section kalau ada hasil search ===
    final prefHas = _match('notifikasi reminder update status', q) ||
        _match('dark mode tema gelap', q) ||
        _match('biometrik fingerprint face id', q) ||
        _match('auto backup simpan otomatis', q);

    final umumHas = _match('profil nama foto kontak', q) ||
        _match('keamanan pin password perangkat', q) ||
        _match('bahasa indonesia', q) ||
        _match('tema tampilan warna font layout', q);

    final bantuanHas = _match('pusat bantuan faq panduan', q) ||
        _match('kebijakan privasi data penggunaan', q) ||
        _match('tentang aplikasi versi build lisensi', q);

    final showPref = q.isEmpty ? true : prefHas;
    final showUmum = q.isEmpty ? true : umumHas;
    final showBantuan = q.isEmpty ? true : bantuanHas;

    final expPref = q.isEmpty ? _expPref : true;
    final expUmum = q.isEmpty ? _expUmum : true;
    final expBantuan = q.isEmpty ? _expBantuan : true;

    return Scaffold(
      backgroundColor: bg,
      body: Stack(
        children: [
          _GradientBackdropPurple(isDark: isDark), // ✅ Background ikut berubah
          SafeArea(
            child: ListView(
              padding: const EdgeInsets.fromLTRB(16, 12, 16, 18),
              children: [
                // ===== Header =====
                Row(
                  mainAxisAlignment: MainAxisAlignment.spaceBetween,
                  children: [
                    _CircleIcon(
                      icon: Icons.arrow_back_ios_new,
                      onTap: () => Navigator.pop(context),
                      iconColor: ink,
                      surface: iconSurface,
                      borderColor: border,
                    ),
                    Text(
                      'Settings',
                      style: TextStyle(
                        fontSize: 16,
                        fontFamily: 'Encode Sans',
                        fontWeight: FontWeight.w700,
                        color: ink,
                        height: 1.2,
                      ),
                    ),
                    _CircleIcon(
                      icon: Icons.home_filled,
                      iconColor: _purple,
                      onTap: () {
                        Navigator.pushAndRemoveUntil(
                          context,
                          MaterialPageRoute(builder: (_) => const HomeScreen()),
                              (route) => false,
                        );
                      },
                      surface: iconSurface,
                      borderColor: border,
                    ),
                  ],
                ),

                const SizedBox(height: 14),

                // ✅ SEARCH BAR
                _GlassCard(
                  cardColor: card,
                  borderColor: border,
                  child: Row(
                    children: [
                      Icon(Icons.search_rounded, color: muted, size: 20),
                      const SizedBox(width: 10),
                      Expanded(
                        child: TextField(
                          controller: _searchC,
                          onChanged: (_) => setState(() {}),
                          style: TextStyle(
                            color: ink,
                            fontFamily: 'Plus Jakarta Sans',
                            fontWeight: FontWeight.w700,
                            fontSize: 12.5,
                          ),
                          decoration: InputDecoration(
                            border: InputBorder.none,
                            hintText: 'Cari setting...',
                            hintStyle: TextStyle(
                              color: muted,
                              fontFamily: 'Plus Jakarta Sans',
                              fontWeight: FontWeight.w600,
                              fontSize: 12.5,
                            ),
                          ),
                        ),
                      ),
                      if (_searchC.text.isNotEmpty)
                        InkWell(
                          borderRadius: BorderRadius.circular(999),
                          onTap: () {
                            _searchC.clear();
                            setState(() {});
                          },
                          child: Padding(
                            padding: const EdgeInsets.all(6),
                            child: Icon(Icons.close_rounded, color: muted, size: 18),
                          ),
                        ),
                    ],
                  ),
                ),

                const SizedBox(height: 14),

                // ===== Preferensi (collapsible) =====
                if (showPref) ...[
                  _AccordionHeader(
                    title: 'Preferensi',
                    subtitle: 'Pengaturan cepat untuk aplikasi',
                    expanded: expPref,
                    onTap: () => setState(() => _expPref = !_expPref),
                    ink: ink,
                    muted: muted,
                    border: border,
                    card: card,
                    accent: _purple,
                  ),
                  const SizedBox(height: 10),
                  if (expPref)
                    _GlassCard(
                      cardColor: card,
                      borderColor: border,
                      child: Column(
                        children: [
                          _SwitchRow(
                            accent: _purple,
                            icon: Icons.notifications_active_outlined,
                            title: 'Notifikasi',
                            subtitle: 'Reminder & update status',
                            value: _notif,
                            onChanged: (v) => setState(() => _notif = v),
                            ink: ink,
                            muted: muted,
                            tile: tile,
                            tileBorder: tileBorder,
                            isDark: isDark,
                          ),
                          _DividerSoft(color: isDark ? const Color(0x14FFFFFF) : const Color(0x110F172A)),

                          // ===== 2. SWITCH DARK MODE (PANGGIL SERVICE) =====
                          _SwitchRow(
                            accent: _purple,
                            icon: Icons.dark_mode_outlined,
                            title: 'Dark Mode',
                            subtitle: 'Tema gelap',
                            value: isDark, // Ambil nilai dari Global Theme
                            onChanged: (val) {
                              ThemeService.saveTheme(val); // Simpan ke Global
                            },
                            ink: ink,
                            muted: muted,
                            tile: tile,
                            tileBorder: tileBorder,
                            isDark: isDark,
                          ),
                          
                          _DividerSoft(color: isDark ? const Color(0x14FFFFFF) : const Color(0x110F172A)),

                          _SwitchRow(
                            accent: _purple,
                            icon: Icons.fingerprint_rounded,
                            title: 'Biometrik',
                            subtitle: 'Fingerprint / Face ID (dummy)',
                            value: _biometric,
                            onChanged: (v) => setState(() => _biometric = v),
                            ink: ink,
                            muted: muted,
                            tile: tile,
                            tileBorder: tileBorder,
                            isDark: isDark,
                          ),
                        ],
                      ),
                    ),
                  const SizedBox(height: 16),
                ],

                // ===== Umum (collapsible) =====
                if (showUmum) ...[
                  _AccordionHeader(
                    title: 'Umum',
                    subtitle: 'Kelola akun, keamanan, dan tampilan',
                    expanded: expUmum,
                    onTap: () => setState(() => _expUmum = !_expUmum),
                    ink: ink,
                    muted: muted,
                    border: border,
                    card: card,
                    accent: _purple,
                  ),
                  const SizedBox(height: 10),
                  if (expUmum)
                    _GlassCard(
                      cardColor: card,
                      borderColor: border,
                      child: Column(
                        children: [
                          _MenuRow(
                            accent: _purple,
                            icon: Icons.language_rounded,
                            title: 'Bahasa',
                            subtitle: 'Indonesia',
                            onTap: () => _toast(context, 'Bahasa (dummy)'),
                            ink: ink,
                            muted: muted,
                            tile: tile,
                            tileBorder: tileBorder,
                          ),
                          _DividerSoft(color: isDark ? const Color(0x14FFFFFF) : const Color(0x110F172A)),
                          _MenuRow(
                            accent: _purple,
                            icon: Icons.info_outline_rounded,
                            title: 'Tentang Aplikasi',
                            subtitle: 'Versi 1.0.0 Beta',
                            onTap: () => _toast(context, 'Tentang (dummy)'),
                            ink: ink,
                            muted: muted,
                            tile: tile,
                            tileBorder: tileBorder,
                          ),
                        ],
                      ),
                    ),
                  const SizedBox(height: 16),
                ],

                // ===== Logout Button =====
                _GlassCard(
                  cardColor: card,
                  borderColor: border,
                  child: InkWell(
                    borderRadius: BorderRadius.circular(22),
                    onTap: _logout, // Panggil fungsi Logout Beneran
                    child: Container(
                      height: 50,
                      alignment: Alignment.center,
                      decoration: BoxDecoration(
                        borderRadius: BorderRadius.circular(22),
                        border: Border.all(color: const Color(0x22EF4444)),
                        gradient: const LinearGradient(
                          begin: Alignment.topLeft,
                          end: Alignment.bottomRight,
                          colors: [Color(0x0FFF6B6B), Color(0x00FFFFFF)],
                        ),
                      ),
                      child: const Row(
                        mainAxisAlignment: MainAxisAlignment.center,
                        children: [
                          Icon(Icons.logout_rounded, color: Color(0xFFEF4444), size: 18),
                          SizedBox(width: 8),
                          Text(
                            'Keluar Aplikasi',
                            style: TextStyle(
                              color: Color(0xFFEF4444),
                              fontSize: 12.5,
                              fontFamily: 'Plus Jakarta Sans',
                              fontWeight: FontWeight.w900,
                            ),
                          ),
                        ],
                      ),
                    ),
                  ),
                ),

                const SizedBox(height: 8),
              ],
            ),
          ),
        ],
      ),
    );
  }

  void _toast(BuildContext context, String msg) {
    ScaffoldMessenger.of(context).showSnackBar(SnackBar(content: Text(msg)));
  }
}

