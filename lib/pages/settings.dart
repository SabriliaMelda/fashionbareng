// ===== Background gradient (ikut darkMode) =====
class _GradientBackdropPurple extends StatelessWidget {
  final bool isDark;
  const _GradientBackdropPurple({required this.isDark});

  @override
  Widget build(BuildContext context) {
    final bool isDark = Theme.of(context).brightness == Brightness.dark;
    
    if (isDark) {
      return Container(
        decoration: const BoxDecoration(
          gradient: LinearGradient(
            begin: Alignment.topLeft,
            end: Alignment.bottomRight,
            colors: [
              Color(0xFF070A12),
              Color(0xFF0B1220),
              Color(0xFF0F172A),
            ],
          ),
        ),
        child: Stack(
          children: const [
            Positioned(top: -60, right: -40, child: _BlurBlob(size: 220, color: Color(0x223B82F6))),
            Positioned(bottom: -80, left: -60, child: _BlurBlob(size: 260, color: Color(0x224C1D95))),
          ],
        ),
      );
    }

    return Container(
      decoration: const BoxDecoration(
        gradient: LinearGradient(
          begin: Alignment.topLeft,
          end: Alignment.bottomRight,
          colors: [
            Color(0xFFFDF4FF),
            Color(0xFFF8FAFC),
            Color(0xFFF3E8FF),
          ],
        ),
      ),
      child: Stack(
        children: const [
          Positioned(top: -60, right: -40, child: _BlurBlob(size: 220, color: Color(0x556B257F))),
          Positioned(bottom: -80, left: -60, child: _BlurBlob(size: 260, color: Color(0x338B5CF6))),
        ],
      ),
    );
  }
}

class _BlurBlob extends StatelessWidget {
  final double size;
  final Color color;
  const _BlurBlob({required this.size, required this.color});

  @override
  Widget build(BuildContext context) {
    return Container(
      width: size,
      height: size,
      decoration: BoxDecoration(color: color, shape: BoxShape.circle),
    );
  }
}

// ===== Reusable UI =====
class _GlassCard extends StatelessWidget {
  final Widget child;
  final Color cardColor;
  final Color borderColor;

  const _GlassCard({
    required this.child,
    required this.cardColor,
    required this.borderColor,
  });

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.all(14),
      decoration: BoxDecoration(
        color: cardColor,
        borderRadius: BorderRadius.circular(22),
        border: Border.all(color: borderColor),
        boxShadow: const [
          BoxShadow(color: Color(0x12000000), blurRadius: 24, offset: Offset(0, 12)),
        ],
      ),
      child: child,
    );
  }
}

class _CircleIcon extends StatelessWidget {
  final IconData icon;
  final VoidCallback onTap;
  final Color? iconColor;
  final Color surface;
  final Color borderColor;

  const _CircleIcon({
    required this.icon,
    required this.onTap,
    required this.surface,
    required this.borderColor,
    this.iconColor,
  });

  @override
  Widget build(BuildContext context) {
    return InkWell(
      borderRadius: BorderRadius.circular(999),
      onTap: onTap,
      child: Container(
        width: 40,
        height: 40,
        decoration: BoxDecoration(
          borderRadius: BorderRadius.circular(999),
          border: Border.all(color: borderColor),
          color: surface,
        ),
        alignment: Alignment.center,
        child: Icon(icon, size: 20, color: iconColor ?? const Color(0xFF0F172A)),
      ),
    );
  }
}

