              const SizedBox(height: 2),
              Text(
                subtitle,
                style: TextStyle(
                  color: muted,
                  fontSize: 11.5,
                  fontFamily: 'Plus Jakarta Sans',
                  fontWeight: FontWeight.w600,
                ),
              ),
            ],
          ),
        ),
        Switch(
          value: value,
          onChanged: onChanged,
          activeColor: accent,
          inactiveThumbColor: isDark ? const Color(0xFFCBD5E1) : null,
          inactiveTrackColor: isDark ? const Color(0x33475569) : null,
        ),
      ],
    );
  }
}

